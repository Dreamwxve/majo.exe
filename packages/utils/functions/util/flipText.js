/**
 * Flips the given text.
 * @param {string} text The text to flip.
 * @returns {string} The flipped text.
 */
export const flipText = (text) => {
 let result = "";
 for (let i = text.length - 1; i >= 0; i--) {
  const char = text[i];
  const flipped = chars[char];
  result += flipped ? flipped : char;
 }
 return result;
};
